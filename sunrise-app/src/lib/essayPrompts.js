// Helper function to get prompts for a specific college
export function getPromptsForCollege(collegeId, appType) {
  // First check if there are college-specific prompts
  if (collegeSpecificPrompts[collegeId]) {
    return collegeSpecificPrompts[collegeId];
  }
  
  // Otherwise, use the app type prompts
  const normalizedAppType = appType?.toLowerCase().replace(/[_\s]/g, '') || 'other';
  const typeMap = {
    'commonapp': 'commonApp',
    'common': 'commonApp',
    'uc': 'uc',
    'mit': 'mit',
    'coalitionapp': 'coalitionApp',
    'coalition': 'coalitionApp'
  };
  
  const mappedType = typeMap[normalizedAppType] || 'other';
  return essayPrompts[mappedType] || essayPrompts.other;
}
